async def translator_program(text, src_lang, dest_lang):
    translator = Translator()
    translation = await translator.translate(text, src=src_lang, dest=dest_lang)
    return translation.text


async def text_translator():
    src_language = input("What language are you translating from?\n")
    dest_language = input("What language do you want to translate to?\n")
    untranslated_text = input("What do you want to translate?\n")
    print(await translator_program(untranslated_text, src_lang=src_language, dest_lang=dest_language))


import asyncio

asyncio.run(text_translator())
